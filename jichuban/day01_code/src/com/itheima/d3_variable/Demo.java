package com.itheima.d3_variable;

//目标：掌握变量的定义格式，和修改变量格式
public class Demo {
    public static void main(String[] args) {

        //1.定义一个变量，表示你考试的成绩，打印变量的值,比如：95.5分
        double score=95.5;
        System.out.println(score);

        //2.把你的考试成绩修改成100分，打印变量的值
        score=100;
        System.out.println(score);


        //3.把你考试的成绩加10分，再次打印变量的值
        score+=10;
        System.out.println(score);



    }
}
